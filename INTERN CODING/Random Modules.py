#randint method

import random
a=random.randint(1,7)
print(a)

#randrange method

import random
a=random.randrange(1,3)
print(a)

#random()

import random
a=random.random()
print(a)

#uniform

import random
a=random.uniform(1,3)
print(a)

#choice

l=[2,5,6,8,79,80]
a=random.choice(l)
print(a)

#Shuffle

l=[2,5,6,8,79,80]
random.shuffle(l)
print(l)
